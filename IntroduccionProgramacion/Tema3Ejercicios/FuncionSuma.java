public class FuncionSuma {
    public static void suma( int x, int b, int c) {
        int resultado =  x + b + c;
        System.out.println("El resultado de la suma de "+ x + " + "+ b +" + "+ c +" es "+ resultado);
    }
    public static void main(String [] args) {
        suma(3,2,1);
        suma(6,4,2);
    }

}
